#!/usr/bin/env node
/**
 * Migration ordering lint for `supabase/migrations/*.sql`.
 *
 * Catches the class of bug where a new migration references a
 * function, table, policy, or column that doesn't exist in the
 * current or any earlier migration. Without this check, the bug
 * only surfaces at `supabase db reset` time in CI (~3 min
 * turnaround) or, worse, on first apply to staging. The lint
 * runs in <100ms locally and can be wired to pre-commit or
 * pre-push.
 *
 * What it checks:
 *
 *  1. Filename order: lex sort = chrono order. Catches "I added
 *     a `2026-07-04_*` migration before `2026-07-01_*`."
 *  2. Object references: every `public.<name>` reference in a
 *     migration must be defined in the current or an earlier
 *     migration. Catches "I call `consume_triage_token()` but the
 *     function lives in a *later* migration."
 *  3. Idempotency: `CREATE FUNCTION` should use `CREATE OR
 *     REPLACE FUNCTION`. `CREATE POLICY` should be preceded by
 *     `DROP POLICY IF EXISTS`. Catches "running the migration
 *     twice fails because the object already exists."
 *
 * What it does NOT check (run `supabase db reset` in CI for
 * those):
 *
 *  - SQL syntax (postgres parses the file at apply time)
 *  - Cross-schema references (only `public.*` is tracked)
 *  - Function signatures (the lint checks name + arity at best)
 *  - The contents of `BEGIN ... COMMIT` blocks (treats the
 *    whole file as one statement for ordering purposes)
 *
 * Usage:
 *   node scripts/check-migrations.mjs
 *
 * Exit codes:
 *   0 — no issues
 *   1 — at least one issue (printed to stderr)
 *
 * To run on every migration: read all `supabase/migrations/*.sql`
 * in lex order; track `public.<name>` objects defined; check
 * references.
 */

import { readFile, readdir } from "node:fs/promises";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(__dirname, "..");
const MIGRATIONS_DIR = join(REPO_ROOT, "supabase", "migrations");

/**
 * Read a SQL file and split it into a list of top-level
 * statements, stripping comments. We don't run a real SQL
 * parser — the goal is to spot reference patterns, not to
 * validate syntax. The two passes are:
 *  - Statement split on `;` outside of string literals
 *  - Strip `-- line comments` and `/* block comments *​/`
 */
function splitStatements(sql) {
  // Strip block comments first (greedy across newlines).
  const noBlock = sql.replace(/\/\*[\s\S]*?\*\//g, "");
  // Strip line comments.
  const noLine = noBlock.replace(/--[^\n]*/g, "");
  // Split on `;` that ends a line. This is a heuristic — a real
  // SQL parser would respect string literals and dollar-quoted
  // blocks. For our migration files (no embedded `;` inside
  // strings), this is sufficient.
  const statements = noLine
    .split(/;\s*\n/g)
    .map((s) => s.trim())
    .filter((s) => s.length > 0);
  return statements;
}

/**
 * Pull `public.<name>` references from a statement. We catch:
 *  - `public.<name>(` — function call
 *  - `public.<name> ` (followed by a non-identifier char) — table
 *  - `public.<name>;` — last reference in a statement
 *  - `public.<name>\n` — line-broken reference
 *  - `public.<name>.<col>` — qualified column
 *  - `public.<name>` with a closing paren later (function call)
 *
 * The regex is intentionally loose: the goal is to spot
 * references, not to classify them. False positives (a
 * reference to a column called `public_x` is fine — the lint
 * only flags `public.x`) are accepted.
 */
const PUBLIC_REF_RE = /\bpublic\.([a-zA-Z_][a-zA-Z0-9_]*)/g;

/**
 * Object name patterns that *define* something (so the lint
 * can add it to the registry). The list is intentionally
 * narrow — only the patterns that are common in our migrations
 * and that have stable names.
 */
const DEFINE_PATTERNS = [
  { re: /CREATE\s+(OR\s+REPLACE\s+)?FUNCTION\s+public\.([a-zA-Z_][a-zA-Z0-9_]*)/i, kind: "function" },
  { re: /CREATE\s+TABLE(?:\s+IF\s+NOT\s+EXISTS)?\s+public\.([a-zA-Z_][a-zA-Z0-9_]*)/i, kind: "table" },
  { re: /ALTER\s+TABLE\s+(?:IF\s+EXISTS\s+)?public\.([a-zA-Z_][a-zA-Z0-9_]*)/i, kind: "alter-table" },
  { re: /CREATE\s+POLICY\s+([a-zA-Z_][a-zA-Z0-9_]*)\s+ON\s+public\.([a-zA-Z_][a-zA-Z0-9_]*)/i, kind: "policy" },
  { re: /CREATE\s+(UNIQUE\s+)?INDEX(?:\s+IF\s+NOT\s+EXISTS)?\s+([a-zA-Z_][a-zA-Z0-9_]*)\s+ON\s+public\.([a-zA-Z_][a-zA-Z0-9_]*)/i, kind: "index" },
  { re: /CREATE\s+EXTENSION(?:\s+IF\s+NOT\s+EXISTS)?\s+([a-zA-Z_][a-zA-Z0-9_]*)/i, kind: "extension" }
];

/**
 * Implicit definitions: things PostgreSQL creates automatically
 * for certain column types. We register these so cross-file
 * references to them (e.g. `GRANT USAGE ON public.trips_id_seq`)
 * don't produce false positives.
 *  - `<table>_id_seq` for `bigint generated by default as identity` or `serial` / `bigserial`
 *  - `<table>_<col>_seq` for any `GENERATED ... AS IDENTITY` column
 */
const IMPLICIT_DEFINE_PATTERNS = [
  {
    re: /CREATE\s+TABLE(?:\s+IF\s+NOT\s+EXISTS)?\s+public\.([a-zA-Z_][a-zA-Z0-9_]*)[^;]*?([a-zA-Z_][a-zA-Z0-9_]*)\s+(?:bigint|integer|smallint)\s+GENERATED\s+(?:BY\s+DEFAULT|ALWAYS)\s+AS\s+IDENTITY/i,
    nameFrom: (m) => `${m[1]}_${m[2]}_seq`,
    kind: "implicit-sequence"
  },
  {
    re: /CREATE\s+TABLE(?:\s+IF\s+NOT\s+EXISTS)?\s+public\.([a-zA-Z_][a-zA-Z0-9_]*)[^;]*?\bid\s+(?:bigserial|serial)\b/i,
    nameFrom: (m) => `${m[1]}_id_seq`,
    kind: "implicit-sequence"
  }
];

/**
 * Idempotency patterns: a migration is non-idempotent if it
 * declares an object without a guard. We flag missing guards
 * for the most common cases.
 */
const NON_IDEMPOTENT_PATTERNS = [
  {
    re: /CREATE\s+(?!OR\s+REPLACE)(?!TABLE\s+IF\s+NOT\s+EXISTS)(?!EXTENSION\s+IF\s+NOT\s+EXISTS)\s+FUNCTION\s+/i,
    message: "CREATE FUNCTION should use CREATE OR REPLACE FUNCTION for idempotency"
  },
  {
    re: /CREATE\s+POLICY\s+(?!IF\s+NOT\s+EXISTS)/i,
    message: "CREATE POLICY should be preceded by DROP POLICY IF EXISTS for idempotency"
  }
];

/**
 * Run the lint. Returns an array of issue objects:
 *   { file, line, severity, message }
 * Empty array means no issues.
 */
function lintFile(filePath, contents, definedBefore) {
  const issues = [];
  const statements = splitStatements(contents);
  // Track what THIS file defines, separately from the cross-file
  // registry. We use this to allow intra-file references (a
  // function used after it's defined in the same file).
  const definedInThisFile = new Set();

  statements.forEach((stmt, idx) => {
    // Find definitions. For each pattern, the capture group(s)
    // hold the object name(s) being defined.
    DEFINE_PATTERNS.forEach(({ re, kind }) => {
      const m = stmt.match(re);
      if (m) {
        // For patterns with two capture groups (e.g. CREATE
        // POLICY name ON table), we register both. The first
        // capture is the policy name; the second is the table.
        for (let i = 1; i < m.length; i++) {
          if (m[i]) definedInThisFile.add(m[i]);
        }
      }
    });
    // Find implicit definitions (auto-generated sequences).
    IMPLICIT_DEFINE_PATTERNS.forEach(({ re, nameFrom }) => {
      const m = stmt.match(re);
      if (m) {
        const name = nameFrom(m);
        if (name) definedInThisFile.add(name);
      }
    });

    // Find references. Strip the references that this statement
    // itself defines (a CREATE TABLE public.foo ... also calls
    // into the "public.foo" token).
    PUBLIC_REF_RE.lastIndex = 0;
    const refs = [...stmt.matchAll(PUBLIC_REF_RE)].map((m) => m[1]);
    const nonDefiningRefs = refs.filter((r) => !definedInThisFile.has(r));

    nonDefiningRefs.forEach((ref) => {
      if (!definedBefore.has(ref)) {
        issues.push({
          file: filePath,
          statementIndex: idx,
          severity: "error",
          message: `references public.${ref} which is not defined in any earlier migration`
        });
      }
    });

    // Idempotency checks (only on the statement that contains
    // the relevant CREATE).
    NON_IDEMPOTENT_PATTERNS.forEach(({ re, message }) => {
      if (re.test(stmt)) {
        issues.push({
          file: filePath,
          statementIndex: idx,
          severity: "warning",
          message
        });
      }
    });
  });

  return issues;
}

async function main() {
  const entries = (await readdir(MIGRATIONS_DIR))
    .filter((f) => f.endsWith(".sql"))
    .sort(); // lex sort; we verify the same order == chrono

  if (entries.length === 0) {
    console.log("[migrations] no .sql files in supabase/migrations/ — nothing to lint");
    return;
  }

  // Check filename order: a wrong filename (e.g. an out-of-band
  // date) would be lex-sorted but not chrono. For our naming
  // convention (YYYYMMDDHHMM_*.sql), lex sort = chrono sort
  // when the prefix is a valid date. We don't validate the
  // date itself, just that filenames are unique.
  const seen = new Set();
  for (const f of entries) {
    if (seen.has(f)) {
      console.error(`[migrations] duplicate migration filename: ${f}`);
      process.exit(1);
    }
    seen.add(f);
  }

  // The cross-file registry: names of objects that have been
  // defined in this or any earlier migration. Names are added
  // to the registry *after* a file is processed (so a migration
  // can't reference objects defined in itself in a later
  // statement and have that count as "defined before"). The
  // intra-file check in lintFile() handles within-file ordering.
  const definedBefore = new Set([
    // Supabase built-ins that the migrations assume to exist.
    // Listing them here avoids spurious "undefined" errors.
    "auth",
    "storage",
    "extensions",
    "graphql_public",
    "gen_random_uuid",
    "now"
  ]);

  let allIssues = [];
  for (const filename of entries) {
    const fullPath = join(MIGRATIONS_DIR, filename);
    const contents = await readFile(fullPath, "utf8");
    const issues = lintFile(filename, contents, definedBefore);
    allIssues = allIssues.concat(issues);

    // Now add this file's definitions to the registry so later
    // migrations can reference them.
    const statements = splitStatements(contents);
    statements.forEach((stmt) => {
      DEFINE_PATTERNS.forEach(({ re }) => {
        const m = stmt.match(re);
        if (m) {
          for (let i = 1; i < m.length; i++) {
            if (m[i]) definedBefore.add(m[i]);
          }
        }
      });
      IMPLICIT_DEFINE_PATTERNS.forEach(({ re, nameFrom }) => {
        const m = stmt.match(re);
        if (m) {
          const name = nameFrom(m);
          if (name) definedBefore.add(name);
        }
      });
    });
  }

  if (allIssues.length === 0) {
    console.log(`[migrations] ${entries.length} files lint clean`);
    return;
  }

  // Group by severity and file for readable output.
  const errors = allIssues.filter((i) => i.severity === "error");
  const warnings = allIssues.filter((i) => i.severity === "warning");

  if (errors.length > 0) {
    console.error(`[migrations] ${errors.length} error(s):`);
    for (const e of errors) {
      console.error(`  ${e.file}: stmt #${e.statementIndex} — ${e.message}`);
    }
  }
  if (warnings.length > 0) {
    console.warn(`[migrations] ${warnings.length} warning(s):`);
    for (const w of warnings) {
      console.warn(`  ${w.file}: stmt #${w.statementIndex} — ${w.message}`);
    }
  }

  // Errors fail the lint; warnings are surfaced but non-blocking.
  if (errors.length > 0) {
    process.exit(1);
  }
}

main().catch((err) => {
  console.error("[migrations] lint crashed:", err);
  process.exit(2);
});
